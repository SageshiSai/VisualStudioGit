package JUEGO;

public class Usuario {
    private String nombre;
    private String dni;
    private int edad;



    public String getNombre() {
	return this.nombre;
    }

    public void setNombre(String nombre) {
	this.nombre = nombre;
    }

    public String getDni() {
    return this.dni;
    }

    public void setDni(String dni) {
    this.dni = dni;
    }

    public int getEdad() {
    return this.edad;
    }

    public void setEdad(int edad) {
    this.edad = edad;
    }



}
